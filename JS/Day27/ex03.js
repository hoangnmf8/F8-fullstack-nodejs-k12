// String -> Number
console.log(Number(""));

// Array [] -> Number
console.log(Number([]));
console.log(Number([10]));

// null -> number
console.log(Number(null));

// undefined -> number
console.log(Number(undefined));

// boolean -> number
console.log(Number(true));
console.log(Number(false));

// number -> boolean
console.log(Boolean(-1));
console.log(Boolean(0));
console.log(Boolean(1));
